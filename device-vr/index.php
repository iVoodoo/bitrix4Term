?><h2 class="section-title">Как работают VR-очки</h2>
<p>
	 Существованием этой технологии мы&nbsp;обязаны устройству человеческих глаз. Благодаря бинокулярному зрению&nbsp;(зрение, в&nbsp;котором используются два глаза)&nbsp;мы&nbsp;можем различать не&nbsp;только цвета, но&nbsp;и&nbsp;глубину&nbsp;— так мы&nbsp;определяем расстояние до&nbsp;объектов и&nbsp;поэтому не&nbsp;врезаемся в&nbsp;препятствия.
</p>
<p>
 <span><br>
 </span>
</p>
 <figure>
<p>
</p>
 <img src="https://248006.selcdn.ru/main/upload/setka_images/11401622032020_4722513d69363eb8e301bcbc5a9b6b244943d87f.png" title="Что видит мозг"><br>
 <figcaption style="width: 503.3px;">Что видит мозг</figcaption></figure><br>
<p>
	 Картинки почти идентичны, но&nbsp;можно заметить, что ракурсы немного различаются. Мозг накладывает эти изображения друг на&nbsp;друга так, как это показано на&nbsp;схеме ниже:
</p>
<p>
 <span><img src="https://248006.selcdn.ru/main/upload/setka_images/11401522032020_e3ea06ecc4efe66fd609360c227a5daace25eda6.png"><br>
